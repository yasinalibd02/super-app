import 'dart:typed_data';

import 'package:flutter/material.dart';
import 'package:flutter_svg/flutter_svg.dart';

class ImageWidget extends StatelessWidget {
  final dynamic imagePath;
  final double? height, width, radius;
  final BoxShape shape;
  final BorderRadiusGeometry? customBorderRadius;
  const ImageWidget(
    this.imagePath, {
    super.key,
    this.height = 50.0,
    this.width = 50.0,
    this.radius = 0,
    this.shape = BoxShape.rectangle,
    this.customBorderRadius,
  });

  @override
  Widget build(BuildContext context) {
    bool isUint8ListImg = imagePath is Uint8List;

    ImageProvider getImageProvider() {
      try {
        if (isUint8ListImg) {
          if (imagePath.isNotEmpty) {
            // Check if the Uint8List has valid image data (e.g., non-empty)
            return MemoryImage(imagePath);
          } else {
            // Handle the case of invalid image data for Uint8List
            return MemoryImage(Uint8List.fromList(defaultImage));
          }
        } else {
          if (imagePath.startsWith("http://") ||
              imagePath.startsWith("https://")) {
            //isNetworkImg = true;
            return NetworkImage(imagePath);
          } else if (imagePath.endsWith('.svg')) {
            //isSvgImg = true;
            // Use a package like flutter_svg for SVG support
            return SvgPicture.asset(imagePath) as ImageProvider;
          } else {
            // Assuming the image path refers to a local asset
            return AssetImage(imagePath);
          }
        }
      } catch (e) {
        // ignore: null_check_always_fails
        return null!;
      }
    }

    final borderRadiusType =
        customBorderRadius ?? BorderRadius.circular(radius!);
    return imagePath != ''
        ? Container(
            height: height ?? 50.0,
            width: width ?? 50.0,
            decoration: BoxDecoration(
              shape: shape,
              borderRadius: borderRadiusType,
              image: DecorationImage(
                image: getImageProvider(),
                filterQuality: FilterQuality.high,
                fit: BoxFit.cover,
              ),
            ),
          )
        : Container();
  }
}

